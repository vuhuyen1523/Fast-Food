<div class="b1">
    <p> Được thành lập tại thành phố San Antonio, bang Texas (Mỹ) năm 1952, Texas Chicken là một trong những thương hiệu hàng đầu thế giới về ngành dịch vụ ăn nhanh. Texas Chicken hiện là thương hiệu quốc tế hàng đầu về gà rán, phục vụ trên 3 triệu quý khách mỗi tuần trên toàn hệ thống 1,700 tiệm của Texas Chicken tại nước Mỹ và 26 nước trên toàn thế giới.</p>
    <p>Thương hiệu gà rán Texas cam kết chỉ sử dụng những nguyên liệu gà tươi ngon, chất lượng nhất, không sử dụng thực phẩm đông lạnh. Các món ăn sẽ được chế biến hàng ngày và đảm bảo nóng hổi, giòn ngon khi đem ra bàn phục vụ thực khách.</p>
    <p>Đến thị trường Việt Nam vào cuối năm 2012,  đến nay Texas Chicken đã nhanh chóng phát triển với 30 nhà hàng  tại Tp.HCM, Bình Dương, Nha Trang, Hà Nội, Hải Phòng và Biên Hòa, thu hút đông đảo thực khách đến trải nghiệm các sản phẩm “Gà rán đậm vị Texas”.</p>
